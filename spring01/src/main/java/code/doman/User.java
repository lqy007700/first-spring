package code.doman;

public class User {
    private String username;
    private int age;
    private String desc;

    public String getUsername() {
        return username;
    }

    public int getAge() {
        return age;
    }

    public String getDesc() {
        return desc;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
